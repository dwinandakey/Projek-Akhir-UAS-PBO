    public KeluarParkir(JScrollPane contentScrollPane) throws SQLException {

        this.contentScrollPane = contentScrollPane;
        initComponents();
        loadTableData();

        // Set up the row sorter for the table
        DefaultTableModel model = (DefaultTableModel) keluarTable.getModel();
        rowSorter = new TableRowSorter<>(model);
        keluarTable.setRowSorter(rowSorter);

        // Add a document listener to the search field
        searchField.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                filterTable();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                filterTable();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                filterTable();
            }
        });
    }

    private void filterTable() {
        String text = searchField.getText();
        if (text.trim().length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
        }
    }

    private void loadTableData() {
        DefaultTableModel dtm = (DefaultTableModel) keluarTable.getModel();
        // Refresh table
        dtm.setRowCount(0);

        try {
            List<Parkir> parkirList = Database.getInstance().dataTable();

            // Populate the table
            for (Parkir p : parkirList) {
                dtm.addRow(new Object[]{
                    p.getNama(),
                    p.getNomorKendaraan(),
                    p.getWaktuMasuk(),
                    p.getCivitas(),
                    p.getJenisKendaraan()
                });
            }
        } catch (SQLException ex) {
            System.err.println(ex);
            JOptionPane.showMessageDialog(this, "Gagal mengambil data", "Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }

