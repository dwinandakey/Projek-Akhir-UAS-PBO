    private void keluarTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_keluarTableMouseClicked
        // TODO add your handling code here:
        String nomorKendaraan = keluarTable.getValueAt(keluarTable.getSelectedRow(), keluarTable.getSelectedColumn()).toString();
        if (nomorKendaraan.equals("")) {
            Image error = Toolkit.getDefaultToolkit().getImage(getClass().getResource("/images/errorImage.png"));
            ImageIcon errorImage = new ImageIcon(error);
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!", "Errors", 1, (Icon) errorImage);
        } else {
            try {
                // TODO add your handling code here:
                int berhasil;
                berhasil = Database.getInstance().keluar(nomorKendaraan);
                if (berhasil == 0) {
                    Image error = Toolkit.getDefaultToolkit().getImage(getClass().getResource("/images/errorImage.png"));
                    ImageIcon errorImage = new ImageIcon(error);
                    JOptionPane.showMessageDialog(null, "Nomor kendaraan tidak ditemukan, ulangi!", "Errors", 1, (Icon) errorImage);
                } else {
                    contentScrollPane.setViewportView(new KeluarParkir(contentScrollPane));
                }
            } catch (ParseException ex) {
                Logger.getLogger(KeluarParkir.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(KeluarParkir.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_keluarTableMouseClicked

